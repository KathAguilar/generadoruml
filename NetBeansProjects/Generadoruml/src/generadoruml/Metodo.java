
        package generadoruml;

        class Metodo {
            private final String nombre;
            private final String tipodato;

        public Metodo(String nombre, String tipodato){
            this.nombre = nombre; 
            this.tipodato = tipodato;
        }
        public String getNombre() {
            return nombre;
        }
        public String getTipoDato() {
            return tipodato;
        }
        }
